    @staticmethod
    def genitive(name):
        words = []

        morph = pymorphy2.MorphAnalyzer()
        for i in name:
            word = morph.parse(i)[0]
            words.append(word.inflect({'gent'}).word)
        return words

    @staticmethod
    def ablative(name):
        words = []

        morph = pymorphy2.MorphAnalyzer()
        for i in name:
            word = morph.parse(i)[0]
            words.append(word.inflect({'ablt'}).word)
        return words

